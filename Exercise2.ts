let fruits: string[] = ["apple", "banana", "mango"];
fruits.push("grapes");//add the element at end
console.log(fruits);
fruits.shift();//remove the first element
console.log(fruits);
fruits.splice(2,0,"pineapple");//add pineapple at 2 index
console.log(fruits);
fruits.reverse();//reverse the array
console.log(fruits);
